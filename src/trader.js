function handleDefinitionRequest(intent, session, callback) {
    var sessionAttributes = {};
    var word = intent.slots.Word.value.toUpperCase();
    getContent(DEFINE_ENDPOINT + encodeURIComponent(word))
          .then((response) => GetQuoteFromResponse(response))
          .catch((err) => console.error(err))
          .then((definition) => definitionResponse(definition))
          .then((response) => callback(sessionAttributes, response));
}

// ------- Helper functions to build responses -------

function definitionResponse(definition) {
    return {
        outputSpeech: {
            type: 'PlainText',
            text: definition
        },
        shouldEndSession: true
    };
}


function buildSpeechletResponse(title, output, repromptText, shouldEndSession) {
    return {
        outputSpeech: {
            type: "PlainText",
            text: output
        },
        card: {
            type: "Simple",
            title: title,
            content: output
        },
        reprompt: {
            outputSpeech: {
                type: "PlainText",
                text: repromptText
            }
        },
        shouldEndSession: shouldEndSession
    };
}

function buildResponse(sessionAttributes, speechletResponse) {
    return {
        version: "1.0",
        sessionAttributes: sessionAttributes,
        response: speechletResponse
    };
}
